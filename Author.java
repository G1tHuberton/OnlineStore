public class Author
{

    private String name;
    private String BirthDate;
   

  public Author(String name, String BirthDate) {

        this.name = name;
        this.BirthDate = BirthDate;

    }


    public String getName() {

        return name;

    }

    public String getBirthdate() {

        return BirthDate;

    }

}
